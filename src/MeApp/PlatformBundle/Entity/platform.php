<?php

namespace MeApp\PlatformBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * platform
 */
class platform
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var MeApp\ProductBundle\Entity\Product
     */
    private $products;

    /**
     * @var string
     */
    private $name;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return platform
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }
    /**
     * Constructor
     */
    public function __construct()
    {
        $this->products = new \Doctrine\Common\Collections\ArrayCollection();
    }

    /**
     * Add products
     *
     * @param \MeApp\ProductBundle\Entity\Product $products
     * @return platform
     */
    public function addProduct(\MeApp\ProductBundle\Entity\Product $products)
    {
        $this->products[] = $products;

        return $this;
    }

    /**
     * Remove products
     *
     * @param \MeApp\ProductBundle\Entity\Product $products
     */
    public function removeProduct(\MeApp\ProductBundle\Entity\Product $products)
    {
        $this->products->removeElement($products);
    }

    /**
     * Get products
     *
     * @return \Doctrine\Common\Collections\Collection 
     */
    public function getProducts()
    {
        return $this->products;
    }
}
